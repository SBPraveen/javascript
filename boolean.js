//Boolean of empty string is false
console.log(Boolean(""))
// false

//Boolean of non empty string is true
console.log(Boolean("\t"))
//true
//NOTE \t stands for horizontal tab

//Boolean of undefined is false
console.log(Boolean(undefined))
//false

//Boolean of null is false
console.log(Boolean(null))
//false

//Boolean of an empty object is true
console.log(Boolean({}))
//true

//Boolean of an empty array is true
console.log(Boolean([]))
//true

